// ========== 整数平方根 (内核无 math.h) ==========
static u32 isqrt(u32 n) {
    if (n == 0) return 0;
    u32 x = n;
    u32 y = (x + 1) / 2;
    while (y < x) {
        x = y;
        y = (x + n / x) / 2;
    }
    return x;
}

