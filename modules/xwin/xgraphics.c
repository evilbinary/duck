void xwin_draw_pixel(xwindow_t* win, i32 x, i32 y, u32 color) {
    if (win == NULL || win->framebuffer == NULL) return;
    set_pixel_safe(win, x, y, color);
}

u32 xwin_get_pixel(xwindow_t* win, i32 x, i32 y) {
    if (win == NULL || win->framebuffer == NULL) return 0;
    if (x < 0 || x >= (i32)win->width || y < 0 || y >= (i32)win->height) return 0;
    return win->framebuffer[y * win->width + x];
}

void xwin_draw_line(xwindow_t* win, i32 x1, i32 y1, i32 x2, i32 y2, u32 color) {
    if (win == NULL || win->framebuffer == NULL) return;
    
    i32 dx = x2 - x1;
    i32 dy = y2 - y1;
    i32 sx = (dx >= 0) ? 1 : -1;
    i32 sy = (dy >= 0) ? 1 : -1;
    dx = (dx >= 0) ? dx : -dx;
    dy = (dy >= 0) ? dy : -dy;
    
    if (dx > dy) {
        i32 err = dx / 2;
        i32 y = y1;
        for (i32 x = x1; x != x2; x += sx) {
            set_pixel_safe(win, x, y, color);
            err -= dy;
            if (err < 0) {
                y += sy;
                err += dx;
            }
        }
    } else {
        i32 err = dy / 2;
        i32 x = x1;
        for (i32 y = y1; y != y2; y += sy) {
            set_pixel_safe(win, x, y, color);
            err -= dx;
            if (err < 0) {
                x += sx;
                err += dy;
            }
        }
    }
    set_pixel_safe(win, x2, y2, color);
}

void xwin_draw_rect(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 color) {
    if (win == NULL) return;
    xwin_draw_line(win, x, y, x + w - 1, y, color);
    xwin_draw_line(win, x + w - 1, y, x + w - 1, y + h - 1, color);
    xwin_draw_line(win, x + w - 1, y + h - 1, x, y + h - 1, color);
    xwin_draw_line(win, x, y + h - 1, x, y, color);
}

void xwin_fill_rect(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 color) {
    if (win == NULL || win->framebuffer == NULL) return;
    
    // 裁剪
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > win->width) w = win->width - x;
    if (y + h > win->height) h = win->height - y;
    if (w <= 0 || h <= 0) return;
    
    // 快速填充
    for (u32 py = y; py < y + h; py++) {
        for (u32 px = x; px < x + w; px++) {
            win->framebuffer[py * win->width + px] = color;
        }
    }
}

void xwin_draw_circle(xwindow_t* win, i32 cx, i32 cy, u32 r, u32 color) {
    if (win == NULL || r == 0) return;
    
    i32 x = r;
    i32 y = 0;
    i32 d = 3 - 2 * r;
    
    while (x >= y) {
        set_pixel_safe(win, cx + x, cy + y, color);
        set_pixel_safe(win, cx - x, cy + y, color);
        set_pixel_safe(win, cx + x, cy - y, color);
        set_pixel_safe(win, cx - x, cy - y, color);
        set_pixel_safe(win, cx + y, cy + x, color);
        set_pixel_safe(win, cx - y, cy + x, color);
        set_pixel_safe(win, cx + y, cy - x, color);
        set_pixel_safe(win, cx - y, cy - x, color);
        
        y++;
        if (d > 0) {
            x--;
            d = d + 4 * (y - x) + 10;
        } else {
            d = d + 4 * y + 6;
        }
    }
}

void xwin_fill_circle(xwindow_t* win, i32 cx, i32 cy, u32 r, u32 color) {
    if (win == NULL || r == 0) return;
    
    for (i32 y = -(i32)r; y <= (i32)r; y++) {
        i32 width = r * r - y * y;
        i32 x = (width > 0) ? 1 : 0;
        while (x * x <= width) {
            set_pixel_safe(win, cx + x, cy + y, color);
            set_pixel_safe(win, cx - x, cy + y, color);
            x++;
        }
    }
}

// ========== 高级绘制 ==========

void xwin_draw_ellipse(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 color) {
    if (win == NULL || w == 0 || h == 0) return;
    
    i32 cx = x + w / 2;
    i32 cy = y + h / 2;
    i32 a = w / 2;
    i32 b = h / 2;
    
    i32 a2 = a * a;
    i32 b2 = b * b;
    
    // 绘制四个象限
    for (i32 px = 0; px <= a; px++) {
        i32 py = (a > 0) ? (i32)(b * isqrt(a2 - px * px) / a) : 0;
        set_pixel_safe(win, cx + px, cy + py, color);
        set_pixel_safe(win, cx - px, cy + py, color);
        set_pixel_safe(win, cx + px, cy - py, color);
        set_pixel_safe(win, cx - px, cy - py, color);
    }
    
    for (i32 py = 0; py <= b; py++) {
        i32 px = (b > 0) ? (i32)(a * isqrt(b2 - py * py) / b) : 0;
        set_pixel_safe(win, cx + px, cy + py, color);
        set_pixel_safe(win, cx - px, cy + py, color);
        set_pixel_safe(win, cx + px, cy - py, color);
        set_pixel_safe(win, cx - px, cy - py, color);
    }
}

void xwin_fill_ellipse(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 color) {
    if (win == NULL || w == 0 || h == 0) return;
    
    i32 cx = x + w / 2;
    i32 cy = y + h / 2;
    i32 a = w / 2;
    i32 b = h / 2;
    
    for (i32 py = -b; py <= b; py++) {
        i32 a2 = a * a;
        i32 b2 = b * b;
        i32 py2 = py * py;
        i32 width = a2 - (a2 * py2) / b2;
        i32 px = (width > 0) ? (i32)isqrt(width) : 0;
        
        for (i32 i = -px; i <= px; i++) {
            set_pixel_safe(win, cx + i, cy + py, color);
        }
    }
}

void xwin_draw_round_rect(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 r, u32 color) {
    if (win == NULL) return;
    
    // 直线部分
    xwin_draw_line(win, x + r, y, x + w - r - 1, y, color);
    xwin_draw_line(win, x + w - 1, y + r, x + w - 1, y + h - r - 1, color);
    xwin_draw_line(win, x + r, y + h - 1, x + w - r - 1, y + h - 1, color);
    xwin_draw_line(win, x, y + r, x, y + h - r - 1, color);
    
    // 四个角
    i32 cx, cy;
    i32 rr = r;
    
    // 左上角
    cx = x + r; cy = y + r;
    for (i32 px = 0; px <= rr; px++) {
        i32 py = (rr > 0) ? (i32)isqrt(rr * rr - px * px) : 0;
        set_pixel_safe(win, cx - rr + px, cy - py, color);
    }
    
    // 右上角
    cx = x + w - r - 1; cy = y + r;
    for (i32 px = 0; px <= rr; px++) {
        i32 py = (rr > 0) ? (i32)isqrt(rr * rr - px * px) : 0;
        set_pixel_safe(win, cx + rr - px, cy - py, color);
    }
    
    // 左下角
    cx = x + r; cy = y + h - r - 1;
    for (i32 px = 0; px <= rr; px++) {
        i32 py = (rr > 0) ? (i32)isqrt(rr * rr - px * px) : 0;
        set_pixel_safe(win, cx - rr + px, cy + py, color);
    }
    
    // 右下角
    cx = x + w - r - 1; cy = y + h - r - 1;
    for (i32 px = 0; px <= rr; px++) {
        i32 py = (rr > 0) ? (i32)isqrt(rr * rr - px * px) : 0;
        set_pixel_safe(win, cx + rr - px, cy + py, color);
    }
}

void xwin_fill_round_rect(xwindow_t* win, i32 x, i32 y, u32 w, u32 h, u32 r, u32 color) {
    if (win == NULL) return;
    
    // 中间矩形
    xwin_fill_rect(win, x + r, y, w - 2 * r, h, color);
    xwin_fill_rect(win, x, y + r, w, h - 2 * r, color);
    
    // 四个角
    i32 rr = r;
    
    // 左上角
    for (i32 py = 0; py < rr; py++) {
        i32 px = (rr > 0) ? (i32)isqrt(rr * rr - py * py) : 0;
        for (i32 i = 0; i <= px; i++) {
            set_pixel_safe(win, x + r - px + i - 1, y + r - py - 1, color);
        }
    }
    
    // 右上角
    for (i32 py = 0; py < rr; py++) {
        i32 px = (rr > 0) ? (i32)isqrt(rr * rr - py * py) : 0;
        for (i32 i = 0; i <= px; i++) {
            set_pixel_safe(win, x + w - r + px - i, y + r - py - 1, color);
        }
    }
    
    // 左下角
    for (i32 py = 0; py < rr; py++) {
        i32 px = (rr > 0) ? (i32)isqrt(rr * rr - py * py) : 0;
        for (i32 i = 0; i <= px; i++) {
            set_pixel_safe(win, x + r - px + i - 1, y + h - r + py, color);
        }
    }
    
    // 右下角
    for (i32 py = 0; py < rr; py++) {
        i32 px = (rr > 0) ? (i32)isqrt(rr * rr - py * py) : 0;
        for (i32 i = 0; i <= px; i++) {
            set_pixel_safe(win, x + w - r + px - i, y + h - r + py, color);
        }
    }
}

// ========== 文本绘制 ==========

void xwin_draw_char(xwindow_t* win, i32 x, i32 y, char ch, u32 color, u32 size) {
    if (win == NULL || win->framebuffer == NULL) return;
    if (ch < 0 || ch >= 128) return;
    
    const u8* glyph = font8x16[(u32)ch];
    if (glyph == NULL) return;
    
    u32 scale = size / 16;
    if (scale < 1) scale = 1;
    
    for (u32 row = 0; row < 16; row++) {
        u8 bits = glyph[row];
        for (u32 col = 0; col < 8; col++) {
            if (bits & (0x80 >> col)) {
                for (u32 sy = 0; sy < scale; sy++) {
                    for (u32 sx = 0; sx < scale; sx++) {
                        set_pixel_safe(win, x + col * scale + sx, y + row * scale + sy, color);
                    }
                }
            }
        }
    }
}

void xwin_draw_text(xwindow_t* win, i32 x, i32 y, const char* text, u32 color) {
    xwin_draw_text_size(win, x, y, text, color, 16);
}

void xwin_draw_text_size(xwindow_t* win, i32 x, i32 y, const char* text, u32 color, u32 size) {
    if (win == NULL || text == NULL) return;
    
    u32 scale = size / 16;
    if (scale < 1) scale = 1;
    u32 char_width = 8 * scale;
    
    i32 cx = x;
    while (*text) {
        xwin_draw_char(win, cx, y, *text, color, size);
        cx += char_width;
        text++;
    }
}

u32 xwin_text_width(const char* text, u32 size) {
    if (text == NULL) return 0;
    u32 scale = size / 16;
    if (scale < 1) scale = 1;
    return kstrlen(text) * 8 * scale;
}

u32 xwin_text_height(u32 size) {
    u32 scale = size / 16;
    if (scale < 1) scale = 1;
    return 16 * scale;
}

// ========== 图像操作 ==========

void xwin_blit(xwindow_t* win, i32 x, i32 y, const u32* data, u32 w, u32 h) {
    if (win == NULL || win->framebuffer == NULL || data == NULL) return;
    
    for (u32 py = 0; py < h; py++) {
        for (u32 px = 0; px < w; px++) {
            i32 sx = x + px;
            i32 sy = y + py;
            if (sx >= 0 && sx < (i32)win->width && sy >= 0 && sy < (i32)win->height) {
                win->framebuffer[sy * win->width + sx] = data[py * w + px];
            }
        }
    }
}

void xwin_blit_transparent(xwindow_t* win, i32 x, i32 y, const u32* data, u32 w, u32 h, u32 key) {
    if (win == NULL || win->framebuffer == NULL || data == NULL) return;
    
    for (u32 py = 0; py < h; py++) {
        for (u32 px = 0; px < w; px++) {
            u32 color = data[py * w + px];
            if (color != key) {
                i32 sx = x + px;
                i32 sy = y + py;
                if (sx >= 0 && sx < (i32)win->width && sy >= 0 && sy < (i32)win->height) {
                    win->framebuffer[sy * win->width + sx] = color;
                }
            }
        }
    }
}

void xwin_copy_area(xwindow_t* win, i32 sx, i32 sy, u32 w, u32 h, i32 dx, i32 dy) {
    if (win == NULL || win->framebuffer == NULL) return;
    
    // 处理重叠区域
    if (dy > sy || (dy == sy && dx > sx)) {
        // 从后往前复制
        for (i32 py = h - 1; py >= 0; py--) {
            for (i32 px = w - 1; px >= 0; px--) {
                i32 src_y = sy + py;
                i32 src_x = sx + px;
                i32 dst_y = dy + py;
                i32 dst_x = dx + px;
                
                if (src_x >= 0 && src_x < (i32)win->width && src_y >= 0 && src_y < (i32)win->height &&
                    dst_x >= 0 && dst_x < (i32)win->width && dst_y >= 0 && dst_y < (i32)win->height) {
                    win->framebuffer[dst_y * win->width + dst_x] = win->framebuffer[src_y * win->width + src_x];
                }
            }
        }
    } else {
        // 从前往后复制
        for (u32 py = 0; py < h; py++) {
            for (u32 px = 0; px < w; px++) {
                i32 src_y = sy + py;
                i32 src_x = sx + px;
                i32 dst_y = dy + py;
                i32 dst_x = dx + px;
                
                if (src_x >= 0 && src_x < (i32)win->width && src_y >= 0 && src_y < (i32)win->height &&
                    dst_x >= 0 && dst_x < (i32)win->width && dst_y >= 0 && dst_y < (i32)win->height) {
                    win->framebuffer[dst_y * win->width + dst_x] = win->framebuffer[src_y * win->width + src_x];
                }
            }
        }
    }
}

// ========== 清除 ==========

void xwin_clear(xwindow_t* win) {
    if (win == NULL || win->framebuffer == NULL) return;
    xwin_clear_color(win, win->bg_color);
}

void xwin_clear_color(xwindow_t* win, u32 color) {
    if (win == NULL || win->framebuffer == NULL) return;
    
    u32* fb = win->framebuffer;
    u32 count = win->width * win->height;
    
    for (u32 i = 0; i < count; i++) {
        fb[i] = color;
    }
}
