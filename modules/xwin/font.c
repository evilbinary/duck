// ========== 字体 API ==========

u32 xfont_get_width(u32 size) {
    switch (size) {
        case 8: return FONT_WIDTH_8;
        case 16:
        default: return FONT_WIDTH_16;
    }
}

u32 xfont_get_height(u32 size) {
    switch (size) {
        case 8: return FONT_HEIGHT_8;
        case 16:
        default: return FONT_HEIGHT_16;
    }
}

const u8* xfont_get_glyph(char ch, u32 size) {
    u32 idx = (u32)(u8)ch;
    if (idx >= 128) idx = '?';
    
    switch (size) {
        case 8: return font8x8[idx];
        case 16:
        default: return font8x16[idx];
    }
}
